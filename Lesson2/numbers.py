#!/usr/bin/python3
num1, num2 = float(input('Введите первое число: ')), float(input('Введите второе число: '))
print("Сумма:", num1, "+", num2, "=",  num1 + num2)
print("Разность:",num1, "-", num2, "=", num1 - num2)
print("Произведение:", num1, "*", num2, "=", num1 * num2)
print("Частное:", num1, "/", num2, "=", num1 / num2)
print("Возведение в степень:", num1, "**", num2, "=", num1 ** num2)
print("Деление по модулю:", num1, "%", num2, "=", num1 % num2)
print("Целочисленное деление:", num1, "//", num2, "=", num1 // num2)
